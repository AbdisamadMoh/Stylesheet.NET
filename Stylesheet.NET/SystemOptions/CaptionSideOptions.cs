namespace StylesheetNET
{
    /// <summary>
    ///Gets predefined css keyword options for <see cref="CaptionSide"/>
    /// </summary>
    public enum CaptionSideOptions
    {
        /// <summary>
        ///Caption is placed above the table
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'top'</c></list>
        /// </summary>
        Top,
        /// <summary>
        ///Default. Caption is below the table
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'bottom'</c></list>
        /// </summary>
        Bottom,
        /// <summary>
        ///Sets the value to its default value.
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'initial'</c></list>
        /// </summary>
        Initial,
        /// <summary>
        ///Inherits the value from its parent element.
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'inherit'</c></list>
        /// </summary>
        Inherit
    }
}