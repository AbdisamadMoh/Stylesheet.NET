namespace StylesheetNET
{
    /// <summary>
    ///Gets predefined css keyword options for <see cref="TextDecorationStyle"/>
    /// </summary>
    public enum TextDecorationStyleOptions
    {
        /// <summary>
        ///Default. Single line
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'solid'</c></list>
        /// </summary>
        Solid,
        /// <summary>
        ///Double line
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'double'</c></list>
        /// </summary>
        Double,
        /// <summary>
        ///Dotted line
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'dotted'</c></list>
        /// </summary>
        Dotted,
        /// <summary>
        ///Dashed line
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'dashed'</c></list>
        /// </summary>
        Dashed,
        /// <summary>
        ///Wavy line
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'wavy'</c></list>
        /// </summary>
        Wavy,
        /// <summary>
        ///Sets the value to its default value.
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'initial'</c></list>
        /// </summary>
        Initial,
