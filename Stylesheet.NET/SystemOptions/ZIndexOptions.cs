namespace StylesheetNET
{
    /// <summary>
    ///Gets predefined css keyword options for <see cref="ZIndex"/>
    /// </summary>
    public enum ZIndexOptions
    {
        /// <summary>
        ///Default. Inherit parent's ZIndex value.
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'auto'</c></list>
        /// </summary>
        Auto,
        /// <summary>
        ///Sets the ZIndex to its default value.
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'initial'</c></list>
        /// </summary>
        Initial,
        /// <summary>
        ///Inherit parent's ZIndex value.
        ///<br/>
        ///<list type="bullet">It is equavalent to <c>'inherit'</c></list>
        /// </summary>
        Inherit
    }
}