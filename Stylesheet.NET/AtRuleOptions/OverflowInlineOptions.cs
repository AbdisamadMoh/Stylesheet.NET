namespace StylesheetNET
{
    /// <summary>
    /// Gets Predefined options for <c>OverflowInline</c> media query feature.
    /// </summary>
    public enum OverflowInlineOptions
    {
        /// <summary>
        /// The content that extends beyond the inline axis is not visible
        /// </summary>
        None,
        /// <summary>
        /// The content that extends beyond the inline axis is visible by moving the scrollbar.
        /// </summary>
        Scroll
    }
}