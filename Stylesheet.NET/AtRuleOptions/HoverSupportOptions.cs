namespace Stylesheet.NET
{
    /// <summary>
    /// Gets Predefined options for <c>Hover</c> media query feature.
    /// </summary>
    public enum HoverSupportOptions
    {
        /// <summary>
        /// The user's device (such as a touchscreen) does not have a pointing input mechanism (like mouse) that can be used to hover over elements.
        /// </summary>
        None,
        /// <summary>
        ///  The user's device (browser) has a pointing input mechanism  (like a mouse) that can be used to hover over elements.
        /// </summary>
        Hover
    }
}