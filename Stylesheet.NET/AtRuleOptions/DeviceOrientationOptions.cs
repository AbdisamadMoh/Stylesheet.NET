namespace StylesheetNET
{
    /// <summary>
    /// Gets Predefined options for <c>Orientation</c> media query feature.
    /// </summary>
    public enum DeviceOrientationOptions
    {
        /// <summary>
        /// This value is applied when the height of the viewport exceeds its width, which commonly indicates that a device is held vertically.
        /// </summary>
        Portrait,
        /// <summary>
        ///  This value is applied when the width of the viewport is greater than its height, typically indicating that the device is held in a horizontal orientation.
        /// </summary>
        Landscape
    }
}